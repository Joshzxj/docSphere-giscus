// Type definitions for Photo Gallery

export interface Photo {
  id: string;
  filename: string;
  title?: string;
  description?: string;
  width: number;
  height: number;
  takenAt?: string;
  thumbnailUrl: string; // 400px
  mediumUrl: string; // 1200px
  fullUrl: string; // Original or 2400px
  blurDataURL?: string; // For Next.js Image blur
}

export interface Album {
  id: string;
  title: string;
  description?: string;
  coverPhoto: Photo;
  photos: Photo[];
  createdAt: string;
  photoCount: number;
}

